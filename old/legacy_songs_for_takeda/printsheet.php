		      <div class="intro">
		         <p>Ispirato ed influenzato visceralmente 
		         dal folk americano degli anni 30, dal rock'n 
		         roll degli anni 50, e dal soul degli anni ’60, 
		         Letlo Vin suona, canta e percuote, 
		         accompagnato da due audaci 
		         polistrumentisti.</p> 
		
         <p>Il suo primo concept album si 
		         intitola "Songs for Takeda", dedicato all' 
		         amico scomparso nel 2004. </p>
		
         <p>Le fonti di ispirazione classiche 
		         sono Johnny Cash, Pete Seeger, Sam Cooke, Bruce Springsteen... </p>
		
         <p>Tra gli artisti contemporanei si trova 
		         d'accordo con Mumford & Sons, The 
		         Decemberists, Bon Iver, Wilco, Iron & 
		         Wine...</p> 
		
         <p>Lo show alterna brani dell'autore e cover 
		         rivisitate con personalità da una band 
		         minimale costituita da Letlo alla voce, 
		         chitarra acustica, kickdrum e foot chimes, 
		         Bronco ai cori, Ukulele basso e Cajon e 
		         Rahim ai cori, chitarra elettrica, shaker e 
		         flauto traverso. </p>
		      </div>
		
