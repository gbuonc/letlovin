                     <p>“Songs for Takeda” è il mio primo album.</p>
                   <p>Tutto è iniziato con una manciata di canzoni scritte nel corso di quattro anni difficili, dal 2008 al 2012, per metabolizzare la morte di uno dei miei migliori amici di sempre: Takeda.</p>
                   <p>La maggior parte di quello che ascolterete è frutto di registrazioni notturne, realizzate in casa con la mia voce, una chitarra e un ukulele, ripresi tra il 2012 e il 2013.</p>
                   <p><strong>Gianluca Buoncompagni</strong> e <strong>Lorenzo Raimond</strong>i hanno successivamente aggiunto idee, percussioni, linee di ukukele basso e chitarre elettriche durante innumerevoli sessioni in studio di registrazione, dando così un grande supporto alla creazione di un suono che risultasse riconoscibile come “Letlo Vin.”</p>
                   <p>Le tracce sono poi state missate nel Gennaio 2014 da <strong>Federico Bortoletto</strong>, che con infinita pazienza e professionalità ha magicamente riordinato in un'unica alchimia il caos generato dal gruppo. </p>
                   <p>I brani sono infine stati masterizzati da <strong>Nick Petersen</strong>, alla cui esperienza ci siamo totalmente affidati, considerato l'ottimo lavoro già svolto per “For Emma, forever ago” di <strong>Bon Iver</strong>.</p>
