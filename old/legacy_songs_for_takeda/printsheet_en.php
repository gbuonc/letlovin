		<h2>Songs for Takeda</h2>	
	           <p>“Songs for Takeda” is my first album.</p>

<p>It all started with a bunch of songs written over the course of 4 tough years, from 2008 to 2012, to commemorate the life of one of my best friends ever, who died in 2004.</p>

<p>Much of what you'll hear comes from overnight home recordings of my voice, folk guitar and ukulele, made in 2012/13 with a borrowed mic plugged into an old laptop through a very cheap audio interface.</p>

<p>A couple of friends of mine (<strong>Gianluca Buoncompagni</strong> and <strong>Lorenzo Raimondi</strong>) joined in later with ideas, percussions, uku-bass, and electric guitars into countless studio sessions, thus giving a big support to Letlo Vin's sound.</p>

<p>The tracks were then mixed in january 2014 by <strong>Federico Bortoletto</strong> (who magically sorted out the mess we had left) and finally mastered in february 2014 by <strong>Nick Petersen</strong>, whose good skills (already shown in “For Emma, forever ago” by <strong>Bon Iver</strong>) helped us out a lot.</p>

